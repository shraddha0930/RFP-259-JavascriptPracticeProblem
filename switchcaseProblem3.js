let number = 100;

switch(number){
  case 1 : console.log("One");
  break;
  case 10 : console.log("Ten");
  break;
  case 100 : console.log("Hundred");
  break;
  case 1000 : console.log("Thousand");
  break;
  case 10000 : console.log("Ten thousand");
  break;
  default:console.log("Enter wrong input")
}